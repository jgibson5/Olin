public class MyFirstApp {
    /** Class for the "Hello World" Exercise
      * Prints "I Rule!" and "The World".
      */
    public static void main (String[] args) {
    	/** Main function for the MyFirstApp.
    	  * Uses a for loop and if statement as an exercise.
    	  */
		for (int x=0; x<2; x++) {
		    if (x == 0) {
			System.out.println("I Rule!");
		    } else {
			System.out.println("The World");
		    }
		}
    }

}